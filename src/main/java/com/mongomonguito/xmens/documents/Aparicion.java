package com.mongomonguito.xmens.documents;

public class Aparicion {

    private String titulo;
    private String anio;

    public Aparicion() {
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }
}
